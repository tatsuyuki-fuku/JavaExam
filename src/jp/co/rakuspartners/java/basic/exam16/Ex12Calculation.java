package jp.co.rakuspartners.java.basic.exam16;

public class Ex12Calculation {
    static int addition(int num1,int num2){
        return num1+num2;
    }

    static int subtraction(int num1,int num2){
        return num1-num2;
    }

    static int multipliction(int num1,int num2){
        return num1*num2;
    }

    static int division(int num1,int num2){
        return num1/num2;
    }
    
    static int remainder(int num1,int num2){
        return num1%num2;
    }

}
